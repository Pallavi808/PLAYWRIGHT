// Example identifier case styles in JavaScript

// camelCase (common for variables and functions)
var firstName = "Pallavi";
var lastName = "Prakash";

// PascalCase (common for constructor functions and classes)
var FullName = "Pallavi Prakash";

// snake_case (allowed, but less common in JS)
var full_name = "pallavi prakash";

// UPPER_SNAKE_CASE (common for constants)
var MAX_USERS = 5;

// Example output
console.log(firstName);   // camelCase
console.log(lastName);    // camelCase
console.log(FullName);    // PascalCase
console.log(full_name);   // snake_case
console.log(MAX_USERS);   // UPPER_SNAKE_CASE